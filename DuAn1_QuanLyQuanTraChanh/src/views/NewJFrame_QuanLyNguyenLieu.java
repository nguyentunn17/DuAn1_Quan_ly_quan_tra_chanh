    private NguyenLieu getData() {
        String ma = txt_ma.getText().trim();
        String ten = txt_ten.getText().trim();
        String loai = txt_loai.getText().trim();
        String soluongSTR = txt_soluong.getText().trim();
        if (ma.length() == 0
                || ten.length() == 0
                || loai.length() == 0) {
            JOptionPane.showMessageDialog(this, "Ko được để trống");
            return null;
        }

        int soLuong = -1;

        try {
            soLuong = Integer.parseInt(soluongSTR);
            if (soLuong < 0) {
                JOptionPane.showMessageDialog(this,
                        "Số lượng phải là số dương");
                return null;
            }
        } catch (NumberFormatException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this,
                    "Số lượng phải là số nguyên");
            return null;
        }
        NguyenLieu nguyenLieu = new NguyenLieu(ma, ten, loai, soLuong);
        return nguyenLieu;
    }

    public void clear() {
        txt_ma.setText("");
        txt_ten.setText("");
        txt_loai.setText("");
        txt_soluong.setText("");

    }

    public void loatTable() {
        defaultTableModel = (DefaultTableModel) this.tbl_nguyenlieu.getModel();
        defaultTableModel.setRowCount(0);
        for (NguyenLieu nguyenLieu : this.nguyenLieu_service.getAll()) {
            Object[] rowdata = {
                nguyenLieu.getId(), nguyenLieu.getMaNL(), nguyenLieu.getTenNL(), nguyenLieu.getLoaiNL(), nguyenLieu.getSoluong()

            };
            defaultTableModel.addRow(rowdata);

        }

    }

    private String getid(String ma) {
        for (NguyenLieu nguyenLieu : this.nguyenLieu_service.getAll()) {
            if (nguyenLieu.getMaNL().equals(ma)) {
                return nguyenLieu.getId();
            }
        }
        return null;
    }

