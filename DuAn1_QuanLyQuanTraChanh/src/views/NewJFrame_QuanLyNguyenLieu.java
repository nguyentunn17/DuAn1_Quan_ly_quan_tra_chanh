    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        // TODO add your handling code here:
        int row = tbl_nguyenlieu.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Chọn dòng cần sửa");
            return;
        }

        NguyenLieu nguyenLieu = this.getData();
        if (nguyenLieu == null) {
            return;
        }
        String ma = tbl_nguyenlieu.getValueAt(row, 1).toString();
        this.nguyenLieu_service.update(getid(ma), nguyenLieu);
        this.loatTable();
        this.clear();
    }//GEN-LAST:event_btn_suaActionPerformed

