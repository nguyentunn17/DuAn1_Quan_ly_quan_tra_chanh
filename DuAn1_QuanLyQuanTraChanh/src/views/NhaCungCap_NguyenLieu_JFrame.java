    public void loadTable() {
        defaultTableModel = (DefaultTableModel) this.tbl_nhacc.getModel();
        defaultTableModel.setRowCount(0);
        for (NhaCungCap_NguyenLieu cap_NguyenLieu : this.cap_NguyenLieu_sv.getList()) {
            Object[] rowdata = {cap_NguyenLieu.getId(), cap_NguyenLieu.getMancc(), cap_NguyenLieu.getTenncc(), cap_NguyenLieu.getDiachi(), cap_NguyenLieu.getSdt(), cap_NguyenLieu.getEmail()};

            defaultTableModel.addRow(rowdata);
        }

    }

    private NhaCungCap_NguyenLieu getData() {
        String ma = txt_ma.getText().trim();
        String ten = txt_ten.getText().trim();
        String diachi = txt_diachi.getText().trim();
        String sdt = txt_sdt.getText().trim();
        String email = txt_email.getText().trim();
        if (ma.length() == 0
                || ten.length() == 0
                || diachi.length() == 0 || sdt.length() == 0 || email.length() == 0) {
            JOptionPane.showMessageDialog(this, "Ko được để trống");
            return null;
        }
        NhaCungCap_NguyenLieu cap_NguyenLieu = new NhaCungCap_NguyenLieu(ma, ten, diachi, sdt, email);
        return cap_NguyenLieu;

    }

    public void clear() {
        txt_diachi.setText("");
        txt_email.setText("");
        txt_ma.setText("");
        txt_sdt.setText("");
        txt_ten.setText("");

    }

    private String getid(String ma) {
        for (NhaCungCap_NguyenLieu nguyenLieu : this.cap_NguyenLieu_sv.getList()) {
            if (nguyenLieu.getMancc().equals(ma)) {
                return nguyenLieu.getId();
            }
        }
        return null;
    }

