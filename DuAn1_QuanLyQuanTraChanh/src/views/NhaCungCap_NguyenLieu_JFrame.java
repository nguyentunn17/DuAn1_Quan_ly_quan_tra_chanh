    private void tbl_nhaccMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_nhaccMouseClicked
        // TODO add your handling code here:
        int row = this.tbl_nhacc.getSelectedRow();
        if (row == -1) {
            return;
        }
        txt_ma.setText(tbl_nhacc.getValueAt(row, 1).toString());
        txt_ten.setText(tbl_nhacc.getValueAt(row, 2).toString());
        txt_diachi.setText(tbl_nhacc.getValueAt(row, 3).toString());
        txt_sdt.setText(tbl_nhacc.getValueAt(row, 4).toString());
        txt_email.setText(tbl_nhacc.getValueAt(row, 5).toString());
    }//GEN-LAST:event_tbl_nhaccMouseClicked

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
        int row = tbl_nhacc.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Chọn dòng cần xóa");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
                "Xác nhận xóa?");
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        String id = tbl_nhacc.getValueAt(row, 0).toString();
        this.cap_NguyenLieu_sv.delete(id);
        this.loadTable();
        this.clear();
    }//GEN-LAST:event_btn_xoaActionPerformed

